package Atikafitriani;

public class Perbandingan {
	public static void main (String [] args){
		int nilai1 =1;
		int nilai2 =2;
		if (nilai1 == nilai2){
			System.out.println("nilai1 == nilai2");
		
		}
		if (nilai1 != nilai2){
			System.out.println("nilai1 ! = nilai2");
		}
		if (nilai1 > nilai2){
			System.out.println("nilai1 > nilai2");
		}	
		if (nilai1 < nilai2){
			System.out.println("nilai1 < nilai2");
		}
		if (nilai1 <= nilai2){
			System.out.println("nilai1 <= nilai2");
	
		}
	}
}
